dia = int(input('Código do dia da semana: '))

if (dia == 0):
    print('Domingo')
elif (dia == 1):
    print('Segunda-feira')
elif (dia == 2):
    print('Terça-feira')
elif (dia == 3):
    print('Quarta-feira')
elif (dia == 4):
    print('Quinta-feira')
elif (dia == 5):
    print('Sexta-feira')
else:
    print('Sábado')