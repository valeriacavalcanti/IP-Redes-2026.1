dia = int(input('Código do dia da semana: '))

if (dia == 0):
    print('Domingo')
else:
    if (dia == 1):
        print('Segunda-feira')
    else:
        if (dia == 2):
            print('Terça-feira')
        else:
            if (dia == 3):
                print('Quarta-feira')
            else:
                if (dia == 4):
                    print('Quinta-feira')
                else:
                    if (dia == 5):
                        print('Sexta-feira')
                    else:
                        print('Sábado')